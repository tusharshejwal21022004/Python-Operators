#Membership Operator (in,not in)
#Membership Operator is used to check data is available in datatype or not.
#It will return boolean output



list1=[12,45,67,89,15]
data=4
print(data in list1)  #False

list2=[89,56,76,"Jay",56]
print("Jay" in list2 )   #True

#not in operator:

s="i am in pune"
sub="mumbai"

print(sub not in s)  #True


list2=[89,56,76,"Jay",56]
print("Jay" not in list2 )   #False